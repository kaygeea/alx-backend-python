#!/usr/bin/env python3
"""Define function that concatenates 2 strings"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate two strings and return the new string.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        Str: A new string concatenated from the two input strings.
    """
    return str(str1 + str2)
